package for_;

public class for_ {

	public static void main(String[] args) {
		
		// Estrutura de repetição FOR
		
		for (int numero = 0; numero <= 10; numero++) {	
			System.out.println("Numero atual é: " + numero);
		}
		System.out.println();
		
		for (int i = 0; i <= 25; i++) {	
			System.out.println("Numero atual é: " + i);
		}
	}
}
